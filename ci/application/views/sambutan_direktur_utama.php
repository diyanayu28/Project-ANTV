<p>Dalam beberapa tahun terakhir ini, MDIA telah mengalami perjalanan yang luar biasa, didukung oleh kinerja stasiun televisi Free to-Air (FTA) anak perusahaannya yaitu ANTV. Berawal dari posisi stasiun televisi FTA dengan peringkat ketujuh di tahun 2013, kami mulai bertransformasi dengan melaksanakan strategi programming yang fokus membidik segmen wanita, dan sejak itu ANTV secara konsisten memusatkan perhatian pada konten-konten keluarga, anak-anak dan hiburan yang terbukti berhasil meraih tonggak sukses baru setiap tahun. Selayaknya, kita panjatkan puji syukur kepada Allah YME atas pencapaian ini khususnya di tengah volatilitas kondisi politik dan ekonomi yang cukup bergejolak. </p>

<p>Pada tahun 2016, MDIA mencatatkan pendapatan tertinggi yang pernah diraih, dengan pertumbuhan dua digit di atas rata-rata industri sebesar 26,7% yang mencapai Rp 1.756,6 miliar. Laba Usaha meningkat sebesar 38,9% menjadi sebesar Rp 536,7 miliar, dengan laba bersih sebesar Rp 645,6 miliar, meningkat sebesar 151,5% dibandingkan tahun 2015. Hasil yang baik ini tercapai ditengah iklim politik dan ekonomi yang penuh goncangan, dimana perekonomian Indonesia Indonesia tumbuh 5,0% atau hanya 0,2% dari tahun 2015 ke tahun 2016, dibayangi prospek pemilihan Kepala Daerah Tingkat 1 dan Tingkat 2 pada awal 2017 serta isu-isu amnesti pajak yang menciptakan suasana ketidakpastian. Di tengah kondisi tersebut, ANTV terus menghadirkan program-program yang segar, menghibur dan menarik yang berhasil memikat hati pemirsa wanita yang ditargetkan. Paduan konten global dengan konten domestik yang senantiasa ditingkatkan, dirancang untuk menarik penonton dan mempertahankan reputasi ANTV sebagai trendsetter dalam industri ini. Untuk mengidentifikasi dan menciptakan konten yang tepat dan menarik bagi pemirsa merupakan tantangan tersendiri, namun tim ANTV memiliki reputasi yang baik dalam hal ini dan terus membuktikan kemampuannya.</p>

<p>ANTV  terus menyiarkan seri domestik, dilengkapi oleh serial India yang tetap populer bagi pemirsa. Program Pesbukers dinilai sebagai program hiburan komedi TV nomor satu. Program anak-anak juga mengalami kenaikan pada akhir tahun. Terkait aspek pemasaran, ANTV masih terus meminta bintang acaranya untuk berinteraksi langsung dengan para penggemar dalam acara jumpa fans maupun melalui media sosial, sebagai strategi untuk meningkatkan interaksi dan keterikatan pemirsanya. Secara paralel, ANTV terus berinvestasi dalam pengembangan sumber daya manusia dan peningkatan infrastruktur dan fasilitas penyiarannya seperti studio dan kamera, serta Master Control Room. Investasi tersebut penting untuk menjaga kemampuan bersaing, dalam rangka memenuhi harapan pemirsa sesuai standar yang layak bagi sebuah stasiun televisi FTA Tier 1. Secara khusus, kami menyadari adanya kebutuhan mencari talent baru untuk mendukung inovasi yang berkelanjutan mengingat industri kami terus berkembang untuk menghasilkan sesuatu yang baru. </p>

<p>ANTV diharapkan dapat memberikan pertumbuhan yang berkelanjutan pada tahun 2017, didukung oleh kepiawaian ANTV dalam mengidentifikasikan program yang menghibur serta strategi programing yang efektif, sehingga citra perusahaan maupun minat pemirsa semakin meningkat. Pada saat yang sama, menyambung keberhasilan ANTV sebagai stasiun televisi FTA Tier 1, MDIA mulai melangkah maju menjajaki diversifikasi lini bisnis dalam rangka memastikan pertumbuhan ke depan. Melengkapi bisnis televisi FTA ANTV, MDIA kini tengah membangun sebuah talent agency yang ditujukan untuk mendukung pertumbuhan ANTV. Selain itu, MDIA juga terus fokus untuk meningkatkan kapasitas produksi program in-house. Ketiga lini bisnis ini diharapkan dapat saling mendukung untuk menghasilkan sinergi, dan pada akhirnya diharapkan akan memiliki kemampuan untuk menyumbangkan tambahan aliran pendapatan bagi Perseroan.</p>

<p>ANTV dan MDIA senantiasa menekankan penerapan prinsip-prinsip tata kelola perusahaan yang baik, termasuk kepatuhan kepada peraturan. Pada tanggal 14 Oktober 2016, ANTV berhasil mendapatkan per panjangan Ijin Penyelenggaraan Penyiaran (IPP) televisi FTA untuk 10 tahun berikutnya dari Menteri Komunikasi dan Informatika Republik Indonesia berdasarkan Surat Keputusan No. 1817 tahun 2016. Hal ini membuktikan bahwa ANTV tercatat sebagai stasiun televisi FTA yang patuh terhadap peraturan. Kami akan terus menekankan kepatuhan terhadap peraturan perundangan yang berlaku sebagai mitra pemerintah dan warga korporasi yang baik. Selain itu, MDIA melalui ANTV juga konsisten melakukan kegiatan sosial serta program yang membantu masyarakat yang kurang beruntung dan kaum dhuafa, sekaligus mempererat hubungan ANTV dengan masyarakat.
Adalah tahun yang luar biasa untuk MDIA dan ANTV, dan saya bangga atas kerja keras dan pencapaian karyawan kami. Saya juga ingin mengucapkan terima kasih kepada Dewan Komisaris, pemirsa setia kami, mitra terpercaya dan para pemegang saham yang kami hargai, yang selama ini menjadi bagian dari perjalanan kami dengan tujuan mewarnai kehidupan sehari-hari. Saya optimis bahwa kami dapat mempertahankan prestasi kami dan bahkan semakin baik, dengan tujuan dapat memberikan manfaat kepada semua pemangku kepentingan.</p>

